

const nome = document.getElementById("cc");

nome.onclick = ( ) => {
	const variavel = nome.getAttribute("src");

	if (variavel==="imagens/chinchila-comum.png"){
		nome.setAttribute("src","imagens/chinchila-comum2.webp");
	} else {
		nome.setAttribute("src","imagens/chinchila-comum.png");
	}
};

